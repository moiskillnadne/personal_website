import classNames from 'classnames';

type Props = {
  value: string;

  size?: 'xs' | 'sm' | 'md' | 'lg' | 'xl' | '2xl' | '3xl' | '4xl' | '5xl' | '6xl';
  weight?:
    | 'thin'
    | 'extralight'
    | 'light'
    | 'normal'
    | 'medium'
    | 'semibold'
    | 'bold'
    | 'extrabold'
    | 'black';
};

function Typography(props: Props) {
  const sizeClassesMap = {
    xs: 'text-xs',
    sm: 'text-sm',
    md: 'text-md',
    lg: 'text-lg',
    xl: 'text-xl',
    '2xl': 'text-2xl',
    '3xl': 'text-3xl',
    '4xl': 'text-4xl',
    '5xl': 'text-5xl',
    '6xl': 'text-6xl',
  };

  const weightClassesMap = {
    thin: 'font-thin',
    extralight: 'font-extralight',
    light: 'font-light',
    normal: 'font-normal',
    medium: 'font-medium',
    semibold: 'font-semibold',
    bold: 'font-bold',
    extrabold: 'font-extrabold',
    black: 'font-black',
  };

  const defaultSize = 'md';

  const defaultWeight = 'normal';

  const size = props.size ?? defaultSize;

  const weight = props.weight ?? defaultWeight;

  const combinedClass = classNames(sizeClassesMap[size], weightClassesMap[weight]);

  return <p className={combinedClass}>{props.value}</p>;
}

export default Typography;
